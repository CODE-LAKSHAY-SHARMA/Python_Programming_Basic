# even or odd

num = int(input("enter the number:"))
if num % 2 == 0:
    print("Number is even!")
else:
    print("Number is odd")

# number divisible by 2 and 5
num1 = int(input("enter the number:"))
if num1 % 2 == 0 and num1 % 5 == 0:
    print("divisible by both 2 and 5")
else:
    print("not divisible")
